package MODELO;

public class Cliente {
    private int Cliente_Id;
    private String Cliente_Nome;
    private String Cliente_Cpf;
    private String Cliente_Tel;
    private String Cliente_Cidade;
    private String Cliente_Rua;
    private int Cliente_Numeracao;

    public int getCliente_Id() {
        return Cliente_Id;
    }

    public void setCliente_Id(int cliente_Id) {
        Cliente_Id = cliente_Id;
    }

    public String getCliente_Nome() {
        return Cliente_Nome;
    }

    public void setCliente_Nome(String cliente_Nome) {
        Cliente_Nome = cliente_Nome;
    }

    public String getCliente_Cpf() {
        return Cliente_Cpf;
    }

    public void setCliente_Cpf(String cliente_Cpf) {
        Cliente_Cpf = cliente_Cpf;
    }

    public String getCliente_Tel() {
        return Cliente_Tel;
    }

    public void setCliente_Tel(String cliente_Tel) {
        Cliente_Tel = cliente_Tel;
    }

    public String getCliente_Cidade() {
        return Cliente_Cidade;
    }

    public void setCliente_Cidade(String cliente_Cidade) {
        Cliente_Cidade = cliente_Cidade;
    }

    public String getCliente_Rua() {
        return Cliente_Rua;
    }

    public void setCliente_Rua(String cliente_Rua) {
        Cliente_Rua = cliente_Rua;
    }

    public int getCliente_Numeracao() {
        return Cliente_Numeracao;
    }

    public void setCliente_Numeracao(int cliente_Numeracao) {
        Cliente_Numeracao = cliente_Numeracao;
    }
}
